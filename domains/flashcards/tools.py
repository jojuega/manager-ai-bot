def _schema(name: str, description: str, properties: dict, required: list[str]) -> dict:
    """Tiny helper to keep the tool schema definitions DRY."""
    return {
        "type": "function",
        "function": {
            "name": name,
            "description": description,
            "parameters": {
                "type": "object",
                "properties": properties,
                "required": required,
            },
        },
    }


