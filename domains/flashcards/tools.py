"""
domains.flashcards.tools — LLM-callable tools for the flashcards domain.

Extracted from the original ``llm_agent.py`` monolith.  Each tool
corresponds to one OpenAI/DeepSeek tool definition registered into
:class:`agent.tool_registry.ToolRegistry` by :func:`register`.

Naming
------
The tool names match the prefixes the LLM expects (see
``agent.prompts.SYSTEM_PROMPT``):

* ``flashcard_*`` — conceptual flashcards (the only kind the LLM can
  manage directly; factual and Notion cards live in Notion).
* ``course_*`` — read-only course listing.

Why not read factual / Notion cards?
------------------------------------
The original SYSTEM_PROMPT tells the LLM: "Solo puedes gestionar
flashcards CONCEPTUALES. Las factuales y de Notion son de solo
lectura (viven en Notion)."  ``flashcard_list`` and
``flashcard_search`` *do* return factual/Notion cards (with a
``card_type`` discriminator) so the LLM can answer questions about
them, but it must not edit or delete them.
"""
