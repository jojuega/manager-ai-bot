# --------------------------------------------------------------------------- #
# Tool dispatcher (name -> callable)
# --------------------------------------------------------------------------- #
_TOOL_FNS: dict[str, Callable] = {
    "flashcard_create_conceptual": tool_flashcard_create_conceptual,
    "flashcard_list": tool_flashcard_list,
    "flashcard_edit": tool_flashcard_edit,
    "flashcard_delete": tool_flashcard_delete,
    "flashcard_reset_srs": tool_flashcard_reset_srs,
    "flashcard_courses": tool_flashcard_courses,
    "flashcard_search": tool_flashcard_search,
}


# Tools that mutate user data — the agent should route these through
# its confirmation flow before invoking them.
_DESTRUCTIVE: frozenset[str] = frozenset({"flashcard_delete"})


# --------------------------------------------------------------------------- #
# Public registration entry point
# --------------------------------------------------------------------------- #
def register(registry: ToolRegistry) -> None:
    """Register every flashcard tool into ``registry``.

    The application entry point is expected to call this once, at
    startup, before instantiating the agent.
    """
    for name, fn in _TOOL_FNS.items():
        registry.add(
            name=name,
            fn=fn,
            schema=_SCHEMAS[name],
            destructive=(name in _DESTRUCTIVE),
        )


